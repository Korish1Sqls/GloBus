function setDates() {
    const today = new Date();
    const year = today.getFullYear();
    const month = String(today.getMonth() + 1).padStart(2, '0');
    const day = String(today.getDate()).padStart(2, '0');
    AppState.search.departDate = `${year}-${month}-${day}`;
    
    const nextWeek = new Date(today);
    nextWeek.setDate(today.getDate() + 7);
    const nextYear = nextWeek.getFullYear();
    const nextMonth = String(nextWeek.getMonth() + 1).padStart(2, '0');
    const nextDay = String(nextWeek.getDate()).padStart(2, '0');
    AppState.search.returnDate = `${nextYear}-${nextMonth}-${nextDay}`;
    
    const departInput = document.getElementById('departDate');
    const returnInput = document.getElementById('returnDate');
    if (departInput) departInput.value = AppState.search.departDate;
    if (returnInput) returnInput.value = AppState.search.returnDate;
}

