        try {
            if (typeof qrcode !== 'undefined') {
                const qr = qrcode(0, 'M');
                qr.addData(qrData);
                qr.make();
                
                const img = qr.createImgTag(5);
                qrContainer.innerHTML = img;
                
                const imgElement = qrContainer.querySelector('img');
                if (imgElement) {
                    imgElement.style.width = '200px';
                    imgElement.style.height = '200px';
                    imgElement.style.borderRadius = '8px';
                }
            } else {
                this.createFallbackQR(qrContainer, qrData);
            }
        } catch (e) {
            console.error('Ошибка создания QR-кода:', e);
            this.createFallbackQR(qrContainer, qrData);
        }
