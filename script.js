const Search = {
    initAutocomplete() {
        const fromInput = document.getElementById('fromLocation');
        const toInput = document.getElementById('toLocation');
        const fromSuggestions = document.getElementById('fromSuggestions');
        const toSuggestions = document.getElementById('toSuggestions');
        
        if (fromInput) {
            fromInput.addEventListener('input', () => {
                const query = fromInput.value.toLowerCase();
                if (query.length < 2) {
                    fromSuggestions.classList.remove('active');
                    return;
                }
                
                const matches = CITIES.filter(city => 
                    city.name.toLowerCase().includes(query) || 
                    city.country.toLowerCase().includes(query)
                ).slice(0, 5);
                
                if (matches.length > 0) {
                    fromSuggestions.innerHTML = matches.map(city => 
                        `<div class="suggestion-item" data-city='${JSON.stringify(city)}'>
                            <span class="city-name">${city.name}</span>
                            <span class="country-name">${city.country}</span>
                        </div>`
                    ).join('');
                    fromSuggestions.classList.add('active');
                } else {
                    fromSuggestions.classList.remove('active');
                }
            });
        }
        
        if (toInput) {
            toInput.addEventListener('input', () => {
                const query = toInput.value.toLowerCase();
                if (query.length < 2) {
                    toSuggestions.classList.remove('active');
                    return;
                }
                
                const matches = CITIES.filter(city => 
                    city.name.toLowerCase().includes(query) ||
                    city.country.toLowerCase().includes(query)
                ).slice(0, 5);
                
                if (matches.length > 0) {
                    toSuggestions.innerHTML = matches.map(city => 
                        `<div class="suggestion-item" data-city='${JSON.stringify(city)}'>
                            <span class="city-name">${city.name}</span>
                            <span class="country-name">${city.country}</span>
                        </div>`
                    ).join('');
                    toSuggestions.classList.add('active');
                } else {
                    toSuggestions.classList.remove('active');
                }
            });
        }
        
        if (fromSuggestions) {
            fromSuggestions.addEventListener('click', (e) => {
                const item = e.target.closest('.suggestion-item');
                if (item) {
                    const city = JSON.parse(item.dataset.city);
                    fromInput.value = `${city.name}, ${city.country}`;
                    fromSuggestions.classList.remove('active');
                    AppState.search.fromCoords = city.coords;
                    this.performSearch();
                }
            });
        }
        
        if (toSuggestions) {
            toSuggestions.addEventListener('click', (e) => {
                const item = e.target.closest('.suggestion-item');
                if (item) {
                    const city = JSON.parse(item.dataset.city);
                    toInput.value = `${city.name}, ${city.country}`;
                    toSuggestions.classList.remove('active');
                    AppState.search.toCoords = city.coords;
                    this.performSearch();
                }
            });
        }
        
        const departDate = document.getElementById('departDate');
        if (departDate) {
            departDate.addEventListener('change', (e) => {
                AppState.search.departDate = e.target.value;
                this.performSearch();
            });
        }
    },
    
    swapLocations() {
        const fromInput = document.getElementById('fromLocation');
        const toInput = document.getElementById('toLocation');
        
        if (fromInput && toInput) {
            [fromInput.value, toInput.value] = [toInput.value, fromInput.value];
            [AppState.search.fromCoords, AppState.search.toCoords] = [AppState.search.toCoords, AppState.search.fromCoords];
            
            MapManager.drawRoute(AppState.search.fromCoords, AppState.search.toCoords);
            this.performSearch();
        }
    },
    
    performSearch() {
        const fromInput = document.getElementById('fromLocation');
        const toInput = document.getElementById('toLocation');
        const departDate = document.getElementById('departDate');
        
        if (!fromInput || !toInput || !departDate) return;
        
        const fromValue = fromInput.value;
        const toValue = toInput.value;
        const departValue = departDate.value;
        
        AppState.search.fromLocation = fromValue;
        AppState.search.toLocation = toValue;
        AppState.search.departDate = departValue;
        
        let fromCity = null;
        let toCity = null;
        
        for (const city of CITIES) {
            if (fromValue.includes(city.name)) fromCity = city;
            if (toValue.includes(city.name)) toCity = city;
        }
        
        if (fromCity) AppState.search.fromCoords = fromCity.coords;
        if (toCity) AppState.search.toCoords = toCity.coords;
        
        MapManager.drawRoute(AppState.search.fromCoords, AppState.search.toCoords);
        
        AppState.data.trips = generateTripsForRoute(fromCity, toCity, departValue);
        AppState.data.filteredTrips = [...AppState.data.trips];
        
        this.applyFilters();
        
        AppState.ui.currentPageNum = 1;
        Render.renderTrips();
    },
    
    applyFilters() {
        const timeFrom = document.getElementById('timeFrom');
        const timeTo = document.getElementById('timeTo');
        const priceMin = document.getElementById('priceMin');
        const priceMax = document.getElementById('priceMax');
        const companySelect = document.getElementById('filterCompany');
        
        const timeFromValue = timeFrom ? timeFrom.value : '';
        const timeToValue = timeTo ? timeTo.value : '';
        const priceMinValue = priceMin ? priceMin.value : '';
        const priceMaxValue = priceMax ? priceMax.value : '';
        const selectedCompanies = companySelect ? Array.from(companySelect.selectedOptions).map(opt => opt.value) : [];
        
        AppState.data.filteredTrips = AppState.data.trips.filter(trip => {
            if (timeFromValue && timeToValue) {
                if (trip.departureTime < timeFromValue || trip.departureTime > timeToValue) return false;
            }
            if (priceMinValue && trip.price < parseInt(priceMinValue)) return false;
            if (priceMaxValue && trip.price > parseInt(priceMaxValue)) return false;
            if (selectedCompanies.length > 0 && !selectedCompanies.includes(trip.company.id)) return false;
            return true;
        });
        
        // После применения фильтров, сортируем по текущему значению select
        const sortBy = document.getElementById('sortTrips')?.value || 'time-asc';
        Render.sortTrips(AppState.data.filteredTrips, sortBy);
    }
};

