const BonusManager = {
    addBonusTransaction(userEmail, type, amount, description) {
        const users = Storage.load('busgo_users', {});
        const user = users[userEmail];
        
        if (!user) return false;
        
        if (!user.bonusHistory) {
            user.bonusHistory = [];
        }
        
        const transaction = {
            id: 'bonus_' + Date.now() + '_' + Math.random().toString(36).substr(2, 9),
            type: type,
            amount: amount,
            description: description,
            date: new Date().toISOString(),
            balance: user.bonus || 0
        };
        
        user.bonusHistory.unshift(transaction);
        
        if (type === 'plus') {
            user.bonus = (user.bonus || 0) + amount;
        } else if (type === 'minus') {
            user.bonus = Math.max(0, (user.bonus || 0) - amount);
        }
        
        users[userEmail] = user;
        Storage.save('busgo_users', users);
        
        if (AppState.auth.currentUser && AppState.auth.currentUser.email === userEmail) {
            AppState.auth.currentUser.bonus = user.bonus;
            AppState.auth.currentUser.bonusHistory = user.bonusHistory;
            Auth.updateUI();
        }
        
        return true;
    },
    
    getUserBonusHistory(email) {
        const users = Storage.load('busgo_users', {});
        const user = users[email];
        return user?.bonusHistory || [];
    },
    
    renderBonusHistory() {
        const user = AppState.auth.currentUser;
        if (!user) return;
        
        const history = this.getUserBonusHistory(user.email);
        const container = document.getElementById('bonusHistory');
        const profileHistoryContainer = document.getElementById('bonusHistoryList');
        
        if (history.length === 0) {
            const emptyHtml = `<p class="empty-state">${LanguageManager.translate('bonus_history')} ${LanguageManager.translate('empty') || 'пуст'}</p>`;
            if (container) container.innerHTML = emptyHtml;
            if (profileHistoryContainer) profileHistoryContainer.innerHTML = emptyHtml;
            return;
        }
        
        const renderItems = (items) => items.map(item => `
            <div class="bonus-history-item">
                <div class="bonus-history-icon ${item.type}">
                    <i class="fas ${item.type === 'plus' ? 'fa-plus' : 'fa-minus'}"></i>
                </div>
                <div class="bonus-history-content">
                    <div class="bonus-history-title">${item.description}</div>
                    <div class="bonus-history-date">${new Date(item.date).toLocaleString()}</div>
                </div>
                <div class="bonus-history-amount ${item.type}">
                    ${item.type === 'plus' ? '+' : '-'}${item.amount} ₸
                </div>
            </div>
        `).join('');
        
        if (container) container.innerHTML = renderItems(history);
        if (profileHistoryContainer) profileHistoryContainer.innerHTML = renderItems(history);
    }
};

