const MapManager = {
    init() {
        if (!document.getElementById('map')) return;
        
        AppState.ui.map = L.map('map', {
            center: [48.0, 68.0],
            zoom: 5,
            zoomControl: false
        });
        
        L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
            attribution: '© OpenStreetMap'
        }).addTo(AppState.ui.map);
        
        L.control.zoom({ position: 'topright' }).addTo(AppState.ui.map);
        
        this.drawRoute(AppState.search.fromCoords, AppState.search.toCoords);
    },
    
    drawRoute(fromCoords, toCoords) {
        if (!AppState.ui.map) return;
        
        if (AppState.ui.routingControl) {
            AppState.ui.map.removeControl(AppState.ui.routingControl);
        }
        
        if (typeof L.Routing !== 'undefined' && L.Routing.control) {
            AppState.ui.routingControl = L.Routing.control({
                waypoints: [
                    L.latLng(fromCoords[0], fromCoords[1]),
                    L.latLng(toCoords[0], toCoords[1])
                ],
                routeWhileDragging: true,
                showAlternatives: true,
                fitSelectedRoutes: true,
                lineOptions: {
                    styles: [{ color: '#2563eb', opacity: 0.8, weight: 5 }]
                }
            }).addTo(AppState.ui.map);
        }
        
        this.updateDistance(fromCoords, toCoords);
    },
    
    updateDistance(fromCoords, toCoords) {
        const distance = calculateDistance(fromCoords, toCoords);
        const routeDistance = document.getElementById('routeDistance');
        if (routeDistance) {
            routeDistance.innerHTML = `<span data-i18n="distance">${LanguageManager.translate('distance')}</span>: ${Math.round(distance)} км`;
        }
    },
    
    reset() {
        if (AppState.ui.map) {
            AppState.ui.map.setView([48.0, 68.0], 5);
        }
    }
};

