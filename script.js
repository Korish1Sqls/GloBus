function generateTripsForRoute(fromCity, toCity, date) {
    const trips = [];
    const numTrips = Math.floor(Math.random() * 15) + 10;
    
    for (let i = 0; i < numTrips; i++) {
        const company = COMPANIES[Math.floor(Math.random() * COMPANIES.length)];
        const busType = AppState.data.busTypes[Math.floor(Math.random() * AppState.data.busTypes.length)];
        
        const hour = Math.floor(Math.random() * 24);
        const minute = Math.floor(Math.random() * 60);
        const departureTime = `${hour.toString().padStart(2, '0')}:${minute.toString().padStart(2, '0')}`;
        
        let basePrice = 0;
        if (fromCity && toCity && fromCity.coords && toCity.coords) {
            const distance = calculateDistance(fromCity.coords, toCity.coords);
            basePrice = Math.floor(distance * 0.8) + 1000;
            const variation = 0.7 + Math.random() * 0.6;
            basePrice = Math.floor(basePrice * variation);
        } else {
            const priceOptions = [1850, 2350, 2900, 3450, 4200, 5800, 7350, 9200, 11800, 15600, 19800, 23500];
            basePrice = priceOptions[Math.floor(Math.random() * priceOptions.length)];
        }
        
        const busTypeMultiplier = {
            'Standard': 1.0,
            'Comfort': 1.3,
            'VIP': 2.0,
            'Luxury': 2.8
        };
        
        basePrice = Math.floor(basePrice * (busTypeMultiplier[busType] || 1.0));
        basePrice = Math.round(basePrice / 50) * 50;
        
        const amenities = [];
        AMENITIES.forEach(amenity => {
            if (Math.random() > 0.4) amenities.push(amenity.id);
        });
        
        const duration = Math.floor(Math.random() * 12) + 3;
        const arrivalHour = (hour + duration) % 24;
        const arrivalTime = `${arrivalHour.toString().padStart(2, '0')}:${minute.toString().padStart(2, '0')}`;
        
        const totalSeats = busType === 'VIP' ? 30 : (busType === 'Luxury' ? 20 : 45);
        const availableSeats = Math.floor(Math.random() * totalSeats * 0.8) + 1;
        
        trips.push({
            id: `trip-${Date.now()}-${i}-${Math.random().toString(36).substr(2, 9)}`,
            from: fromCity || { name: AppState.search.fromLocation.split(',')[0], country: 'Қазақстан' },
            to: toCity || { name: AppState.search.toLocation.split(',')[0], country: 'Қазақстан' },
            company: company,
            busType: busType,
            departureTime: departureTime,
            departureDate: date,
            arrivalTime: arrivalTime,
            duration: duration,
            price: basePrice,
            amenities: amenities,
            totalSeats: totalSeats,
            availableSeats: availableSeats
        });
    }
    
    trips.sort((a, b) => a.departureTime.localeCompare(b.departureTime));
    return trips;
}

function calculateDistance(coords1, coords2) {
    const R = 6371;
    const dLat = (coords2[0] - coords1[0]) * Math.PI / 180;
    const dLon = (coords2[1] - coords1[1]) * Math.PI / 180;
    const a = Math.sin(dLat/2) * Math.sin(dLat/2) +
              Math.cos(coords1[0] * Math.PI / 180) * Math.cos(coords2[0] * Math.PI / 180) * 
              Math.sin(dLon/2) * Math.sin(dLon/2);
    const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
    return R * c;
}

