    getFilteredReviews() {
        let reviews = [...AppState.data.reviews];
        
        const companyFilter = document.getElementById('reviewCompanyFilter')?.value || 'all';
        const ratingFilter = document.getElementById('reviewRatingFilter')?.value || 'all';
        const sortFilter = document.getElementById('reviewSort')?.value || 'newest';
        
        if (companyFilter !== 'all') {
            reviews = reviews.filter(r => r.company === companyFilter);
        }
        
        if (ratingFilter !== 'all') {
            const minRating = parseInt(ratingFilter);
            reviews = reviews.filter(r => r.rating >= minRating);
        }
        
        reviews.sort((a, b) => {
            switch(sortFilter) {
                case 'newest': return new Date(b.date) - new Date(a.date);
                case 'oldest': return new Date(a.date) - new Date(b.date);
                case 'highest': return b.rating - a.rating;
                case 'lowest': return a.rating - b.rating;
                default: return 0;
            }
        });
        
        return reviews;
    },
    
    renderReviews(loadMore = false) {
        const container = document.getElementById('reviewsGrid');
        if (!container) return;
        
        const filteredReviews = this.getFilteredReviews();
        const totalReviews = filteredReviews.length;
        
        if (!loadMore) {
            AppState.ui.reviewsPage = 1;
        }
        
        const start = 0;
        const end = AppState.ui.reviewsPage * AppState.ui.reviewsPerPage;
        const paginatedReviews = filteredReviews.slice(start, end);
        
        if (paginatedReviews.length === 0) {
            container.innerHTML = `<p class="empty-state">${LanguageManager.translate('no_results')}</p>`;
        } else {
            container.innerHTML = paginatedReviews.map(review => this.renderReviewCard(review)).join('');
        }
        
        const loadMoreBtn = document.getElementById('loadMoreReviews');
        if (loadMoreBtn) {
            loadMoreBtn.style.display = end < totalReviews ? 'block' : 'none';
        }
    },
    
