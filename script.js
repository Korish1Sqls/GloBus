const Render = {
    renderTrips() {
        const container = document.getElementById('tripsList');
        if (!container) return;
        
        const sortBy = document.getElementById('sortTrips')?.value || 'time-asc';
        
        // Применяем сортировку к отфильтрованным рейсам
        this.sortTrips(AppState.data.filteredTrips, sortBy);
        
        const start = (AppState.ui.currentPageNum - 1) * AppState.ui.itemsPerPage;
        const end = start + AppState.ui.itemsPerPage;
        const paginatedTrips = AppState.data.filteredTrips.slice(start, end);
        
        if (paginatedTrips.length === 0) {
            container.innerHTML = `<p style="text-align: center; padding: 2rem;">${LanguageManager.translate('no_results')}</p>`;
            this.renderPagination(0);
            return;
        }
        
        container.innerHTML = paginatedTrips.map(trip => this.renderTripCard(trip)).join('');
        
        this.renderPagination(AppState.data.filteredTrips.length);
        
        const tripsCount = document.getElementById('tripsCount');
        if (tripsCount) tripsCount.textContent = AppState.data.filteredTrips.length;
        
        // Обновляем язык для кнопок
        LanguageManager.translateTripCards();
    },
    
    renderTripCard(trip) {
        return `
            <div class="trip-card" data-trip-id="${trip.id}">
                <div class="trip-info">
                    <div class="trip-time">
                        <div class="departure-time">${trip.departureTime}</div>
                        <div>→ ${trip.arrivalTime}</div>
                        <small>${trip.duration} ${LanguageManager.translate('hours')}</small>
                    </div>
                    
                    <div class="trip-route">
                        <div><strong>${trip.from.name}</strong> → <strong>${trip.to.name}</strong></div>
                        <small>${trip.departureDate}</small>
                    </div>
                    
                    <div class="trip-company">
                        <div>${trip.company.name}</div>
                        <small>${trip.busType}</small>
                    </div>
                    
                    <div class="trip-amenities">
                        ${trip.amenities.map(a => {
                            const amenity = AMENITIES.find(am => am.id === a);
                            return amenity ? `<i class="fas ${amenity.icon}" title="${amenity.name}"></i>` : '';
                        }).join('')}
                    </div>
                </div>
                
                <div class="trip-price-block">
                    <div class="current-price">${trip.price.toLocaleString()} ₸</div>
                    <small>${trip.availableSeats} ${LanguageManager.translate('seats_short')}</small>
                    <button class="buy-btn" onclick="openPurchaseModal('${trip.id}')" 
                            ${!AppState.auth.isAuthenticated ? 'disabled' : ''}>
                        ${AppState.auth.isAuthenticated ? LanguageManager.translate('buy_btn') : LanguageManager.translate('login_prompt')}
                    </button>
                </div>
            </div>
        `;
    },
    
    sortTrips(trips, sortBy) {
        trips.sort((a, b) => {
            switch(sortBy) {
                case 'time-asc':
                    return a.departureTime.localeCompare(b.departureTime);
                case 'time-desc':
                    return b.departureTime.localeCompare(a.departureTime);
                case 'price-asc':
                    return a.price - b.price;
                case 'price-desc':
                    return b.price - a.price;
                default:
                    return 0;
            }
        });
    },
    
    renderPagination(total) {
        const container = document.getElementById('pagination');
        if (!container) return;
        
        const totalPages = Math.ceil(total / AppState.ui.itemsPerPage);
        if (totalPages <= 1) {
            container.innerHTML = '';
            return;
        }
        
        let html = '';
        
        if (AppState.ui.currentPageNum > 1) {
            html += `<button class="page-btn" data-page="${AppState.ui.currentPageNum - 1}"><i class="fas fa-chevron-left"></i></button>`;
        }
        
        for (let i = 1; i <= totalPages; i++) {
            if (i === 1 || i === totalPages || (i >= AppState.ui.currentPageNum - 2 && i <= AppState.ui.currentPageNum + 2)) {
                html += `<button class="page-btn ${i === AppState.ui.currentPageNum ? 'active' : ''}" data-page="${i}">${i}</button>`;
            }
        }
        
        if (AppState.ui.currentPageNum < totalPages) {
            html += `<button class="page-btn" data-page="${AppState.ui.currentPageNum + 1}"><i class="fas fa-chevron-right"></i></button>`;
        }
        
        container.innerHTML = html;
        
        container.querySelectorAll('.page-btn').forEach(btn => {
            btn.addEventListener('click', () => {
                const page = parseInt(btn.dataset.page);
                if (!isNaN(page)) {
                    AppState.ui.currentPageNum = page;
                    Render.renderTrips();
                }
            });
        });
    },
    
    renderPopularRoutes() {
        const container = document.getElementById('popularRoutes');
        if (!container) return;
        
        container.innerHTML = AppState.data.popularRoutes.map(route => `
            <div class="popular-card" onclick="searchPopularRoute('${route.from}', '${route.to}')">
                <i class="fas fa-route"></i>
                <div class="popular-route">${route.from} → ${route.to}</div>
                <div class="popular-price">${LanguageManager.translate('from')} ${route.price.toLocaleString()} ₸</div>
                <div class="popular-count">${route.count} ${LanguageManager.translate('trips')}</div>
            </div>
        `).join('');
    }
};

